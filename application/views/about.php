<h3>Our Mission</h3>
<p>Great Plant Picks is an educational program committed to building a comprehensive palette of outstanding plants for maritime Pacific Northwest gardens. Great Plant Picks is one of the educational outreach programs for the Elisabeth C. Miller Botanical Garden, providing a forum for sharing horticultural information with the wider gardening community.</p>
<h3>Goals &amp; Objectives</h3>
<dl class="leaf">
<dt>Create a comprehensive list of hardy, reliable plants for home gardeners and industry professionals specifically focused on the maritime Northwest.</dt>

    <dd>Assemble selection committees with representation from Western British Columbia, Western Oregon and Western Washington with representation from a wide variety of horticultural backgrounds.</dd>
    <dd>Develop plant assessment programs and expert resources for the in-depth research of a plant&#8217;s suitability for the region.</dd>
</dl>
<dl class="leaf">
<dt>Provide easily accessible information on these selected plants.</dt>
    <dd>Furnish information on the best cultural practices with accurate characteristics on the selected plants.</dd>
    <dd>Network with the horticultural community to distribute GPP information.</dd>
</dl>
<dl class="leaf">
<dt>Provide and clarify accurate nomenclature and identification of the selected plants.</dt>
    <dd>Investigate through the most current resources the proper botanical names of the selected plants.</dd>
    <dd>Make available information to help the average person acquire the right plant.</dd>
    <dd>Use the highest quality photographs to show the best features of the selected plants.</dd>
   </dl>
