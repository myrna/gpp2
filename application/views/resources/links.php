        <dt>Master Gardener Program</dt>
        <dd>The origin of this now internationally widespread program began in the Pacific Northwest.  In the early 1970s Cooperative Extension
        Service Agents in the Washington state counties of King and Pierce began to experience an increasingly high demand for information from urban
         gardeners.  This prompted the development of the Master Gardener program to train volunteers in the basics of horticulture and, in return for
         their education, volunteers commit to answering questions from the gardening public, thus allowing the Extension Agents the time to fulfill
         broader educational programming.  To contact a Master Gardner:</dd>
